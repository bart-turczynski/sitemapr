# ---- html --------------------------------------------------------------------

test_that("DOCTYPE html classifies as html", {
  body <- paste0(
    "<!DOCTYPE html><html><head><title>Hi</title></head>",
    "<body></body></html>"
  )
  expect_identical(sniff(charToRaw(body)), "html")
})

test_that("leading <html> tag classifies as html", {
  body <- "<html lang=\"en\"><body>x</body></html>"
  expect_identical(sniff(charToRaw(body)), "html")
})

test_that("HTML masquerading inside markup is html, not an xml sitemap", {
  # An error page that mentions urlset in text but is really HTML.
  body <- paste0(
    "<!doctype html>\n",
    "<html><body><p>No urlset here, just a 404.</p></body></html>"
  )
  expect_identical(sniff(charToRaw(body)), "html")
})

# ---- text --------------------------------------------------------------------

test_that("plain text list of URLs classifies as text", {
  body <- "https://example.com/a\nhttps://example.com/b\n"
  expect_identical(sniff(charToRaw(body)), "text")
})

test_that("UTF-8 text with high bytes is still text", {
  body <- "café résumé über\n"
  expect_identical(sniff(charToRaw(enc2utf8(body))), "text")
})

# ---- binary ------------------------------------------------------------------

test_that("content with a NUL byte classifies as binary", {
  body <- as.raw(c(charToRaw("PK"), 0x00, 0x03, charToRaw("more")))
  expect_identical(sniff(body), "binary")
})

test_that("dense control bytes (no NUL) classify as binary", {
  ctrl <- as.raw(rep(0x01L, 100L))
  expect_identical(sniff(ctrl), "binary")
})
