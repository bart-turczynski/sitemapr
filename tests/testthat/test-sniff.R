# Byte-level format sniffer unit tests (Layer B + fetch_classification.feature
# sniff scenarios). Internal fns are referenced via sitemapr:::.
#
# Inputs are built as raw vectors: charToRaw() for text/markup, as.raw() for
# fixed magic bytes, gzfile()+writeBin() for real gzip-container bytes (NB:
# memCompress(., "gzip") emits a raw zlib stream "78 9c", NOT a gzip container
# with the 1f 8b magic, so it cannot be used to exercise the sniffer), and a
# synthesized 512-byte block with "ustar" at offset 257 for an uncompressed tar.

sniff <- function(x) sitemapr:::sniff_format(x)

# Produce real gzip-container bytes (leading magic 1f 8b) for a raw payload.
gzip_bytes <- function(payload) {
  tf <- tempfile()
  on.exit(unlink(tf), add = TRUE)
  con <- gzfile(tf, "wb")
  writeBin(payload, con)
  close(con)
  readBin(tf, what = "raw", n = file.info(tf)$size)
}

# Build a minimal but valid uncompressed POSIX tar header block: 512 zero bytes
# with the ASCII magic "ustar" placed at offset 257 (0-based).
make_tar_header <- function() {
  block <- raw(512L)
  block[258:262] <- charToRaw("ustar")
  block
}

# ---- empty -------------------------------------------------------------------

test_that("zero-length raw is empty", {
  expect_identical(sniff(raw(0L)), "empty")
})

test_that("NULL is empty", {
  expect_identical(sniff(NULL), "empty")
})

test_that("whitespace-only content is text, not empty", {
  expect_identical(sniff(charToRaw("   \n\t  \r\n")), "text")
})

# ---- gzip --------------------------------------------------------------------

test_that("gzip magic bytes classify as gzip", {
  expect_identical(sniff(as.raw(c(0x1F, 0x8B, 0x08, 0x00))), "gzip")
})

test_that("real gzip-compressed XML classifies as gzip (feature scenario)", {
  xml <- '<?xml version="1.0"?><urlset><url><loc>http://a/</loc></url></urlset>'
  gz <- gzip_bytes(charToRaw(xml))
  expect_identical(sniff(gz), "gzip")
})

test_that("a gzip-wrapped tar (.tar.gz) sniffs as gzip, not tar", {
  payload <- as.raw(c(make_tar_header(), make_tar_header()))
  gz <- gzip_bytes(payload)
  expect_identical(sniff(gz), "gzip")
})

# ---- tar ---------------------------------------------------------------------

test_that("uncompressed POSIX tar header classifies as tar", {
  expect_identical(sniff(make_tar_header()), "tar")
})

test_that("tar magic not at offset 257 does not classify as tar", {
  block <- raw(512L)
  block[1:5] <- charToRaw("ustar")
  # No NUL-free text either, so this falls through to binary (NUL-filled).
  expect_false(identical(sniff(block), "tar"))
})

# ---- xml-urlset --------------------------------------------------------------

test_that("XML urlset classifies as xml-urlset", {
  body <- '<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
  <url><loc>https://example.com/</loc></url>
</urlset>'
  expect_identical(sniff(charToRaw(body)), "xml-urlset")
})

test_that("XML body served as .txt classifies by bytes (feature scenario)", {
  # The file is named sitemap.txt but the BYTES are XML: must be xml-urlset.
  ns <- "http://www.sitemaps.org/schemas/sitemap/0.9"
  body <- paste0(
    "<urlset xmlns=\"", ns, "\">",
    "<url><loc>https://e/</loc></url></urlset>"
  )
  expect_identical(sniff(charToRaw(body)), "xml-urlset")
})

test_that("namespace-prefixed urlset root is recognised", {
  ns <- "http://www.sitemaps.org/schemas/sitemap/0.9"
  body <- paste0(
    "<sm:urlset xmlns:sm=\"", ns, "\"></sm:urlset>"
  )
  expect_identical(sniff(charToRaw(body)), "xml-urlset")
})

test_that("BOM-prefixed XML urlset classifies as xml-urlset", {
  bom <- as.raw(c(0xEF, 0xBB, 0xBF))
  body <- as.raw(c(bom, charToRaw('<urlset xmlns="x"><url/></urlset>')))
  expect_identical(sniff(body), "xml-urlset")
})

test_that("UTF-16 BOM-prefixed XML urlset classifies as xml-urlset", {
  bom <- as.raw(c(0xFE, 0xFF))
  body <- as.raw(c(bom, charToRaw("<urlset></urlset>")))
  expect_identical(sniff(body), "xml-urlset")
})

test_that("XML decl plus comment before the root still finds urlset", {
  body <- '<?xml version="1.0"?>
<!-- generated by the build -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"></urlset>'
  expect_identical(sniff(charToRaw(body)), "xml-urlset")
})

test_that("urlset behind a DOCTYPE with an internal subset is xml-urlset", {
  # The xxe-attempt.xml shape: the internal subset's ENTITY declaration contains
  # its own ">", so a naive "consume to first >" would strand "]>...<urlset>"
  # and misroute to the text classifier. The bracket-skip must reach the root.
  body <- paste0(
    "<?xml version=\"1.0\"?>",
    "<!DOCTYPE urlset [ <!ENTITY xxe SYSTEM \"file:///etc/hostname\"> ]>",
    "<urlset xmlns=\"http://www.sitemaps.org/schemas/sitemap/0.9\">",
    "<url><loc>https://example.com/</loc></url></urlset>"
  )
  expect_identical(sniff(charToRaw(body)), "xml-urlset")
})

test_that("DOCTYPE with no internal subset before urlset still works", {
  body <- paste0(
    "<!DOCTYPE urlset SYSTEM \"sitemap.dtd\">",
    "<urlset xmlns=\"http://www.sitemaps.org/schemas/sitemap/0.9\"></urlset>"
  )
  expect_identical(sniff(charToRaw(body)), "xml-urlset")
})

